  distribution_id,
  is_default_browser,
  activity_segment,
  is_dau,
  is_wau,
  is_mau,
  is_daily_user,
  is_weekly_user,
  is_monthly_user,
FROM
  `moz-fx-data-shared-prod.firefox_ios.usage_reporting_active_users`
WHERE
  mozfun.norm.browser_version_info(app_version).major_version >= 136
