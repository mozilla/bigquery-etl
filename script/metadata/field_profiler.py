"""Profile every column in a BigQuery table — distinct values, frequency, and null rate."""

import logging
import random
import re
import time
from argparse import ArgumentParser
from datetime import datetime, timezone

import anthropic
from google.cloud import bigquery

logging.basicConfig(
    level=logging.INFO, format="%(asctime)s: %(levelname)s: %(message)s"
)

# Cardinality threshold: fields with <= 50 distinct values store the full value + frequency list.
# Fields above the threshold store only the distinct count and one example value (shape, not enumeration)
HIGH_CARDINALITY_THRESHOLD = 50
SKIP_DISTINCT_TYPES = {
    "STRUCT",
    "JSON",
    "RECORD",
    "ARRAY",
}  # COUNT(DISTINCT ...) not supported for these types
TIER3_COLUMNS = {
    "metrics"
}  # wide/repeated STRUCTs deferred to a future job. Not currently profiled.
SOURCE_PROJECT = "moz-fx-data-shared-prod"
DEST_TABLE = "mozdata-nonprod.analysis.gkabbz_data_profiling_test_v1"
CLAUDE_MODEL = "claude-sonnet-4-6"

